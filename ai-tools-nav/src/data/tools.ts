// 工具函数
export const getToolBySlug = (slug: string): AITool | undefined => {
  return tools.find(tool => tool.slug === slug);
};

export const getToolsByCategory = (categoryId: string): AITool[] => {
  return tools.filter(tool => 
    tool.category === categoryId || tool.categories?.includes(categoryId as any)
  );
};

export const getFeaturedTools = (): AITool[] => {
  return tools.filter(tool => tool.isFeatured).slice(0, 8);
};

export const getNewTools = (): AITool[] => {
  return tools.filter(tool => tool.isNew || isRecentlyAdded(tool.addedDate))
    .slice(0, 6);
};

export const getTopRatedTools = (limit: number = 6): AITool[] => {
  return [...tools]
    .sort((a, b) => b.averageRating - a.averageRating)
    .slice(0, limit);
};

export const searchTools = (query: string): AITool[] => {
  const lowerQuery = query.toLowerCase();
  return tools.filter(tool => 
    tool.name.toLowerCase().includes(lowerQuery) ||
    tool.description.toLowerCase().includes(lowerQuery) ||
    tool.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
  );
};

// 辅助函数
function isRecentlyAdded(dateString: string): boolean {
  const addedDate = new Date(dateString);
  const threeMonthsAgo = new Date();
  threeMonthsAgo.setMonth(threeMonthsAgo.getMonth() - 3);
  return addedDate > threeMonthsAgo;
}
